      <h1>Tamofen</h1>
      <img src="obat.png" alt="" class="d-block mx-auto" />
      <h2>Deskripsi</h2>
      <p>Tamofen digunakan untuk membantu mengobati kanker payudara yang telah menyebar ke bagian lain dari tubuh.</p>
      <h2>Pengertian Tamofen</h2>
      <p>
        Tamofen adalah obat yang mengandung Tamoxifen. Tamofen digunakan untuk membantu mengobati kanker payudara yang telah menyebar ke bagian lain dari tubuh, mengobati kanker payudara pada pasien tertentu setelah operasi dan terapi
        radiasi, membantu mengatasi infertilitas anovulasi (ketidaksuburan). Selain itu, Tamofen juga bisa diresepkan untuk mengurangi kemungkinan berkembangnya kanker payudara pada wanita yang memiliki risiko tinggi mengidap penyakit
        tersebut.
      </p>
      <h2>Keterangan Tamofen</h2>
      <p>
        <ul>
            <li>
                Golongan: Obat Keras
            </li>
            <li>
                Kandungan: Tamoxifen 10 mg; Tamoxifen 15 mg; Tamoxifen 30 mg            </li>
            <li>
                Bentuk: Tablet Salut Selaput
            </li>
            <li>
                Satuan Penjualan: Strip
            </li>
            <li>
                Kemasan: Strip @ 10 Tablet Salut Selaput
            </li>
            <li>
                Farmasi: Kalbe Farma
            </li>
            <li>
                Harga: Rp30.000 - Rp60.000/ Strip</p>
            </li>
           
        </ul>
       
        <h2>Kegunaan Tamofen</h2>
        <p>Tamofen digunakan untuk membantu mengobati kanker payudara yang telah menyebar ke bagian lain dari tubuh, mengobati kanker payudara pada pasien tertentu setelah operasi dan terapi radiasi, mengurangi perkembangan kanker payudara, membantu mengatasi infertilitas anovulasi (ketidaksuburan).
        </p>
        <h2>Kegunaan Tamofen</h2>
        <p>Tamofen merupakan golongan obat keras. Obat ini memerlukan resep dokter untuk pembelian serta penggunaannya.</p>
        <ul>
            <li>
                Mengurangi kejadian kanker payudara pada wanita berisiko tinggi Dewasa: 20 mg setiap hari selama 5 tahun.
            </li>
            <li>
                Kanker payudara Dewasa: 20 mg setiap hari sebagai dosis tunggal atau dalam 2 dosis terbagi. Maksimal: 40 mg setiap hari.          </li>
            <li>
                Infertilitas anovulasi Dewasa: 20 mg setiap hari pada hari ke 2-5 dari siklus menstruasi, dosis dapat di tingkatkan jika perlu dalam siklus berikutnya. Maksimal: 80 mg setiap hari.            </li>
            <li>
                Wanita dengan menstruasi tidak teratur: terapi awal dapat dimulai pada hari apa saja dan terapi kedua mungkin dimulai 45 hari kemudian.            </li>
            
        </ul>
<h2>Cara Penyimpanan</h2>
<p>Simpan pada suhu dibawah 30 derajat Celcius, terhindar dari cahaya.</p>
<h2>Efek Samping Tamofen</h2>
<p>Efek samping penggunaan Tamofen yang mungkin terjadi adalah:</p>
<ul>
    <li>Mual</li>
    <li>Perdarahan atau pengeluaran vagina</li>

    <li>Kulit kering, ruam</li>

    <li>Sakit kepala, pusing</li>

    <li>Kram otot</li>

    <li>Trombositopenia (jumlah trombosit kurang dari normal)</li>
    <li>Peningkatan enzim hati</li>
    <li>Hipertrigliseridemia</li>

</ul>
<h2>Kontraindikasi Tamofen </h2>
    <p>Hindari penggunaan Tamofen pada pasien:</p>
    <ul>
        <li>Riwayat DVT atau emboli paru.</li>
        <li>Kehamilan dan menyusui.</li>
        <li>Tidak boleh diberikan bersamaan dengan antikoagulan tipe kumarin.</li>
    </ul>
<h2>Interaksi Obat Tamofen dengan Obat Lain</h2>
<p>Berikut adalah beberapa obat yang dapat berinteraksi dengan Tamofen: </p>
    <ul>
        <li>Warfarin</li>
        <li>Diuretik thiazide</li>

        <li>Tegafur</li>

    </ul>
    <h4>Kategori Kehamilan</h4>
    <p>Tidak direkomendasikan untuk digunakan pada wanita hamil.</p>
    <h4>Perhatian Menyusui</h4>
    <p>Tidak direkomendasikan untuk digunakan pada ibu menyusui.</p>
